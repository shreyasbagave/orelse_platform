// Helper functions for product data
export const getProductsByCategory = (category) => {
  return products.filter(product => product.category.toLowerCase() === category.toLowerCase())
}

export const getProductsByFarmer = (farmerName) => {
  return products.filter(product => 
    product.farmer.toLowerCase().includes(farmerName.toLowerCase())
  )
}

export const getProductsByLocation = (location) => {
  return products.filter(product => 
    product.location.toLowerCase().includes(location.toLowerCase())
  )
}

export const getProductsByPriceRange = (minPrice, maxPrice) => {
  return products.filter(product => {
    const price = parseInt(product.price.replace(/[^\d]/g, ''))
    return price >= minPrice && price <= maxPrice
  })
}

export const getProductsByEcoScore = (minEcoScore) => {
  return products.filter(product => product.eco >= minEcoScore)
}

export const getFeaturedProducts = () => {
  return products.filter(product => product.eco >= 80 && product.rating >= 4.7)
}

export const getNewArrivals = () => {
  // For demo purposes, return products with high eco scores as "new arrivals"
  return products.filter(product => product.eco >= 85)
}

export const getBestSellers = () => {
  return products.filter(product => product.reviews >= 100)
}

export const searchProducts = (query) => {
  const searchTerm = query.toLowerCase()
  return products.filter(product => 
    product.name.toLowerCase().includes(searchTerm) ||
    product.description.toLowerCase().includes(searchTerm) ||
    product.category.toLowerCase().includes(searchTerm) ||
    product.farmer.toLowerCase().includes(searchTerm) ||
    product.location.toLowerCase().includes(searchTerm)
  )
}

